    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $name = trim($request->input("structure"));

        try {
            $structure = Structure::create([
                "name" => $name
            ]);

            $message = "A estrutura '$structure->name' foi inserida com sucesso!";
            $typeMessage = "success";

        } catch (\Throwable $th) {
            $typeMessage = "error";
            $message = $th->getMessage();
        }

        return redirect()->route("structures.create")->with(
            $typeMessage, $message
        );
    }

